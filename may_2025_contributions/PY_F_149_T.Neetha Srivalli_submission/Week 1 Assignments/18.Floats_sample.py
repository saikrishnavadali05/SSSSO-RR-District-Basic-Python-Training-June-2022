celsius = float(input("Enter temperature in Celsius: "))     #defining a variable celcius
fahrenheit = (celcius * 9/5) + 32   #converting celcius into fahrenheit temperature
print("Temperature in celcius: ", celcius)   #prints temperaturs in celcius
print ("Temperature in fahrenheit: ", fahrenheit)   #prints temperature in fahrenheit
