# Python Training 2025 - Logical Operators
# Description : Python program : Read two Boolean values (e.g., True/False) and print the results of and, or, and not on them.
# Author : Praveena Kumbum
# Date : 23 May 2025
Bool_Val1 = True
Bool_Val2 = False

#Logical operator and
print(Bool_Val1 and Bool_Val2)

#Logical operator or
print(Bool_Val1 or Bool_Val2)

#Logical operator not
print(not Bool_Val1)
print(not Bool_Val2)
