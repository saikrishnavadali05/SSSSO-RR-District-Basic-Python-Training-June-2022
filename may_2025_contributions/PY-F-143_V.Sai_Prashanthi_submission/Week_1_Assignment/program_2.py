"""Question 2:
numeric operators:
Ask the user for two whole numbers and print their sum, difference, product, and quotient.

"""
#solution:
num1=int(input("Enter a whole number:")) # Took input from user by displaying message "Enter a whole number:"
num2=int(input("Enter another whole number:")) # Took another input from user by displaying message : "Enter another whole number:"
print("Sum:",num1+num2) #sum using '+' operator
print("Difference:",num1-num2) #difference using '-' operator
print("Product:",num1*num2) # product using '*' operator
print("Quotient:",num1/num2) #Quotient using '/' operator