def numeric_operator():
    a = int(input("Enter first whole number: "))
    b = int(input("Enter second whole number: "))
    sum = a + b
    difference = a - b 
    product = a * b 
    quotient = a / b 
    print ("sum = ", sum)
    print ("difference = ", difference)
    print ("product = ", product)
    print ("quotient = ", quotient)
    
    
    numeric_operator()